public class User {
    public String id, name, phone, password, email, emotion, activity;

    public User() {
    }

    public User(String id, String name, String phone, String password, String email) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.password = password;
        this.email = email;
        this.emotion = emotion;
        this.activity = activity;

    }
}
